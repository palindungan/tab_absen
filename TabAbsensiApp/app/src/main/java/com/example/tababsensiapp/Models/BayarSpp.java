package com.example.tababsensiapp.Models;

public class BayarSpp {
    String id_bayar_spp, id_murid, id_admin, waktu, total_pertemuan, total_spp;

    public String getId_bayar_spp() {
        return id_bayar_spp;
    }

    public void setId_bayar_spp(String id_bayar_spp) {
        this.id_bayar_spp = id_bayar_spp;
    }

    public String getId_murid() {
        return id_murid;
    }

    public void setId_murid(String id_murid) {
        this.id_murid = id_murid;
    }

    public String getId_admin() {
        return id_admin;
    }

    public void setId_admin(String id_admin) {
        this.id_admin = id_admin;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }

    public String getTotal_pertemuan() {
        return total_pertemuan;
    }

    public void setTotal_pertemuan(String total_pertemuan) {
        this.total_pertemuan = total_pertemuan;
    }

    public String getTotal_spp() {
        return total_spp;
    }

    public void setTotal_spp(String total_spp) {
        this.total_spp = total_spp;
    }
}
