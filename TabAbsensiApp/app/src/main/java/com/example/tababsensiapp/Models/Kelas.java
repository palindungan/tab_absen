package com.example.tababsensiapp.Models;

public class Kelas {
    String id_kelas_p, hari, jam_mulai, jam_berakhir, harga_fee, nama_pelajaran, nama_sharing;

    public String getId_kelas_p() {
        return id_kelas_p;
    }

    public void setId_kelas_p(String id_kelas_p) {
        this.id_kelas_p = id_kelas_p;
    }

    public String getHari() {
        return hari;
    }

    public void setHari(String hari) {
        this.hari = hari;
    }

    public String getJam_mulai() {
        return jam_mulai;
    }

    public void setJam_mulai(String jam_mulai) {
        this.jam_mulai = jam_mulai;
    }

    public String getJam_berakhir() {
        return jam_berakhir;
    }

    public void setJam_berakhir(String jam_berakhir) {
        this.jam_berakhir = jam_berakhir;
    }

    public String getHarga_fee() {
        return harga_fee;
    }

    public void setHarga_fee(String harga_fee) {
        this.harga_fee = harga_fee;
    }

    public String getNama_pelajaran() {
        return nama_pelajaran;
    }

    public void setNama_pelajaran(String nama_pelajaran) {
        this.nama_pelajaran = nama_pelajaran;
    }

    public String getNama_sharing() {
        return nama_sharing;
    }

    public void setNama_sharing(String nama_sharing) {
        this.nama_sharing = nama_sharing;
    }
}
